def slug_to_display(slug: str) -> str:
    """Convert StampWorld slug to display name."""
    if slug in TERRITORIES:
        return TERRITORIES[slug]["display_name"]
    # Fallback: replace hyphens with spaces, capitalize words
    return slug.replace("-", " ").title()


def display_to_slug(name: str) -> str:
    """Convert display name to StampWorld slug."""
    # First try exact match in display names
    for slug, data in TERRITORIES.items():
        if data["display_name"] == name:
            return slug
    
    # Try case-insensitive match
    for slug, data in TERRITORIES.items():
        if data["display_name"].lower() == name.lower():
            return slug
    
    # Fallback: replace spaces with hyphens, lowercase
    return name.replace(" ", "-").replace(".", "")


def get_by_region(region: str) -> List[str]:
    """Get all territory slugs for a given region."""
    return [slug for slug, data in TERRITORIES.items() if data["region"] == region]


def get_all_slugs() -> List[str]:
    """Get all territory slugs."""
    return list(TERRITORIES.keys())


def get_all_display_names() -> List[str]:
    """Get all display names."""
    return [data["display_name"] for data in TERRITORIES.values()]


def get_territory(slug: str) -> Optional[Dict]:
    """Get full territory data by slug."""
    return TERRITORIES.get(slug)


def get_collection_path(slug: str) -> Optional[str]:
    """Get collection path for a territory slug."""
    territory = TERRITORIES.get(slug)
    return territory["collection_path"] if territory else None


def get_year_range(slug: str) -> Optional[str]:
    """Get year range for a territory slug."""
    territory = TERRITORIES.get(slug)
    return territory["year_range"] if territory else None


def get_region(slug: str) -> Optional[str]:
    """Get region for a territory slug."""
    territory = TERRITORIES.get(slug)
    return territory["region"] if territory else None


def validate_slug(slug: str) -> bool:
    """Check if a slug exists in the registry."""
    return slug in TERRITORIES


def validate_display_name(name: str) -> bool:
    """Check if a display name exists in the registry."""
    return any(data["display_name"] == name for data in TERRITORIES.values())


